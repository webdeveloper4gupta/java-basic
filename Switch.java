public class Switch {
    public static void main(String[] args) {
        int a=9;
        switch(a){
            case 6:
                System.out.println("a is equal to n6");
                break;
            case 7:
                System.out.println("a is equal to 7");
                break;
            case 8:
                System.out.println("a is equal to 8");
                break;
            case 9:
                System.out.println("a is equal to 9");
                break;
            default:
                System.out.println(" neither");
                break;
        }
    }
}
